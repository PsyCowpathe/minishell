#include	"minishell.h"

void	set_pid(int pid)
{
	int		i;

	i = 0;
	while (i < 1024 && g_pid_t[i] != 0)
		i++;
	g_pid_t[i] = pid;
}

void	init_pid(void)
{
	int		i;

	i = 0;
	while (i < 1024)
		g_pid_t[i++] = 0;
}
